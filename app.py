    def insert_sentence(text, sentence, interval):
        words = text.split()
        new_words = []
        count = 0
    
        for word in words:
            new_words.append(word)
            count += 1
    
            if count % interval == 0:
                new_words.append(sentence)
    
        return ' '.join(new_words)
        
        

